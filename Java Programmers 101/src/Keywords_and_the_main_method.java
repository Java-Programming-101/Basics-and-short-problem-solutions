
/*
 * A keyword is one of 50 reserved words that have a predefined meaning in Java. 
 * Because of this, programmers cannot use keywords as names for variables, methods, 
 * classes, or as any other identifier.
 * 
 * In most development environments they will be highlighted (for example in eclipse 
 * keywords are highlighted in purple. 
 * 
 * keywords are always lower case. 
 */

public class Keywords_and_the_main_method {

	
	/*
	 * The method signature for the main() method contains three modifiers:
	 * public indicates that the main() method can be called by any object. 
	 * static indicates that the main() method is a class method avalible without an instance of that class being instantiated.. 
	 * void indicates that the main() method has no return value.
	 */
	public static void main(String[] args) {

		System.out.println("Hello Java programmers!");

	}

	
}
