

public class Simple_For_Loop {

	public static void main(String[] args) {
		/* This Programme demonstrates 2 basic for loops by iterating through numbers 1 to 10 and displaying the number in 
		 *the console.
		 */
		
		
		int number = 1;
		
		for ( ; number <= 10; number++){
			System.out.println("I have gone through the 1st for loop " + number + " times.");
		}
		
		
		
		
		System.out.println("");
		
		
		
		
		
		for (int i=1; i<= 10 ; i++){
			System.out.println("I have gone through the 2nd for loop " + i + " times.");
		}
		
		
		

	}

}
